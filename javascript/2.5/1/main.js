'use strict';

function multiply(a, b) {
  return a * b;
}
console.log(multiply(2, 8));

function media(a, b, c, d) {
  return (a + b + c + d) / 4;
}
console.log(media(1, 2, 3, 4));

function booly(a) {
  if (a % 2 === 0) {
    return true;
  } else {
    return false;
  }
}
console.log(booly(10));
